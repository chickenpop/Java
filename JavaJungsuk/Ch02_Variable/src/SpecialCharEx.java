public class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\'');           // 작은따음표 넣기, 특수문자는 ' ' 사이에 넣는 거 가능
        System.out.println("abc\t123\b456");// 탭, 백스페이스
        System.out.println('\n');           // 개행
        System.out.println("\"Hello\"");    // 큰따음표 넣기
        System.out.println("c:\\");         // 슬래쉬 넣기
    }    
}
