public class Ex01_OperatorEx1 {

    public static void main(String[] args) {
        
        // 단항 연산자, 증감 연산자 ++
        // 독립적으로 사용한 경우 연산결과가 동일
        // 연산 종료 후 결과가 동일하게 남긴 때문 

        int i = 5;
        i++;
        System.out.println(i);

        i = 5;
        ++i;
        System.out.println(i);



    }
}
