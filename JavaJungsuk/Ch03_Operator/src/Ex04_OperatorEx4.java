public class Ex04_OperatorEx4 {
    
    public static void main(String[] args) {
        
        // 부호 연산자 +, -가 있으니 형식적 추가
        int i = -10;
        i = +i;
        System.out.println(i);

        // 부호 연산자 -, 부호를 반대로 바꿔줌
        // boolean, char을 제외한 값형(기본형)에서 사용가능
        i = -10;
        i = -i;
        System.out.println(i);

    }

}
